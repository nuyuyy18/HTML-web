# Programming = Sebuah proses u/ membuat program dg menuliskan kumpulan kode atau perintah untuk komputer
# ada 3 kategori bahasa pemrograman = Bahasa Mesin, Bahasa Assembly, Bahasa Tingkat Tinggi
# Low-Level Language = Bahasa Mesin dan Bahasa Assembly
# High_level Language = Python, JavaScript dll
# Code Editor = Program u/ Mancatat Text yang Berhubungan dengan kegiatan Programming
# Fitur yang disediakan Code editor = Syntax Highlighting, Indentasi Otomatis, Autocomplete, Extension/Plug-In
# IDE = Integrated Development Environment
# Berisi Fitur = Code Editor (Membantu Menulis Kode)            
#                Compiler/Interpreter (Menerjemahkan kode ke dalam bahasa yang dapat dibaca komputer) 
#                Debuger (u/ Mencegah Kesalahan Ketika coding)
#                GUI (Graphical User Interface) u/ Mempermudah Pembuatan Aplikasi Secara Visual
# Jenis-jenis Tools IDE = VSCode, IntelliJ IDEA, PHP Storm, NetBeans, PyCharm
# Coding = Kegiatan Menulis Baris-baris Kode dari suatu bahasa Pemrograman
# Binary Code Biasanya Dikelompokkan dengan nama Bytes. 
#       Bytes = Kelompok Angka Berisi 8 Digit yang Mewakili 8 Transistor
# Syntax/Susunan Code = Aturan yang Mendefinisikan Kombinasi Penulisan Karakter yang Benar dalam sebuah bahasa pemrograman 
# Semantik = Makna dari Rangkaian kode yang Ditulis
# Kompilasi = Proses Penerjemahan (Agar Barisan Syntax Lebih Dipahami Komputer)
# Compiled Language = Bahasa Pemrograman yang harus diterjemahkan dulu oleh Program yang namanya Compiler
#       Contoh Bahasanya = C, C++, C#, Go, pascal, Rust, Visual Basic, kotlin, Swift
# Source Code = Kode Sumber yang Ditulis oleh Programmer
# Compiler = Program yang Menerjemahkan Kode Sumber ke dalam Bahasa Mesin
# Interpreted Language = Bahasa Pemrograman yang Bisa Langsung Dieksekusi tanpa Penerjemahan
#       Contoh Bahasanya = PHP, Python, JavaScript, ruby, Perl, Lua
# JIT (Just In Time) Language = Bahasa Pemrograman yang Diterjemahkan Compiler saat Program Berjalan 
#       Contoh Bahasanya = C#, Java
# Statement = u/ melakukan Tugas tertentu
#       Unit Sintaks u/ Menyatakan Aksi atau Tugas yang harus Dilakukan 
# Expression = u/ Menghasilkan Nilai
#       Entitas Sintaks yang Menghasilkan sebuah Nilai
#       Terdiri dari = Variabel, Operator, Konstanta, Fungsi
# Syarat Pembuatan Variabel (C) = 1. Nama Variabel dilarang didahului dengan simbol dan angka
#                           = 2. Nama variabel tidak boleh pakai kata kunci yang sudah ada pada bahasa C (IF,int, void dll)
#                           = 3. Bersifat Sensitive, yaitu huruf besar dan kecil dibedakan
#                           = 4. Gunakan Underscore(_) u/ Nama Variabel yang terdiri dari 2 suku kata
# Syarat Pembuatan Variabel (PHP) = 1. Awali dengan tanda Dollar($) diikuti dengan nama variable
#                                 = 2. Angka jangan Ditulis diawal
#                                 = 3. Yang diperbolehkan hanya (_) dan ($)
#                                 = 4. Case sensitive
#                                 = 5. Tidak perlu mendefinisikan tipe data
#                                 = 6. Nama variabel tidak boleh pakai kata kunci yang sudah ada pada bahasa PHP (_GET, _POST, GLOBAL)
# Tipe Data Dibagi 2 :
#       1. Tipe Data Dasar (Primitif)
#           ex: Integer, Float, Char (A,B,C), String, Boolean
#       2. Tipe Data Bentukan (Majemuk)
#           ex: Array,
# Array = Tipe Data Yang Menyimpan banyak Isi Dalam Sebuah Variabel
# Control Flow = cara untuk mengontrol urutan eksekusi pernyataan dalam suatu program
# Cara lain agar program tidak Control Flow (Loop dan Conditiona/Percabangan)
# While Loop = 1. digunakan ketika kita telah menetapkan stop condition sebelumnya 
#              2. blok statement/kode yang sama akan terus dijalankan sampai stop condition ini tercapai 
#              3. stop condition akan di cek sebelum melakukan proses loop
# Conditional = aliran yang mengontrol kode program berdasarkan pengujian pernyataan bersyarat
# Pernyataan Bersyarat dapat berisi = Fungsi, operator matematika, operator logika
# Function = blok kode yang dirancang untuk melakukan tugas tertentu, dan dapat dipanggil berulang kali dalam program. Fungsi membantu memecah program besar menjadi bagian-bagian yang lebih kecil dan terorganisir, serta menghindari pengulangan kode
# Function dibagi menjadi 2 : 1. Built-In Function 
#                                (Fungsi Yang disediakan langsung oleh bahasa pemrograman dan bisa langsung dipakai)
#                                ex = Printf(C), Alert(JS), var_dump(PHP)
#                             2. User-Defined Function
#                                (Fungsi yang Tidak tersedia pada Bahasa Pemrograman, Sehingga perlu dibuat oleh pemrogram)
# Parameter = Sebutan untuk Nilai Inputan Fungsi pada saat Fungsi itu Didefinisikan
# Argumen = Sebutan untuk Nilai Inputan Fungsi pada saat Fungsi itu dipanggil
# Tips Mangatasi Munculnya Bug = 1. Membangun Komunikasi Yang Baik (Tim Kuat + Manajemen Baik)
#                                2. Melakukan Uji Coba : Alpha Testing (Uji Coba Fungsionalitas di Tahap Awal)
#                                                        Beta Testing (Uji Coba Untuk Mamastikan Kasiapan Software sebelum Dipasarkan secara Umum)
#                                3. Manfaatkan Platform Kolaborasi (Github/Gitlab)
# Metodologi Pengembangan Software Agile = Sistem Dikembangkan Berfase dengan Tiap Fase memiliki Tujuan yang Berbeda
# Debugging = Mengidentidikasi, Menghapus, dan Memperbaiki Bug atau Potential error
# Debugging dibagi 2 : 1. Debugging Manual
#                      2. Debugging dengan code editor 
# Macam-Macam Algoritma : 1. Searching (Pencarian data dalam sekumpulan data)
#                         2. Sorting (Pengurutan Data)
